package com.wq.vo;

/**
 * 类文件VO.
 * 
 * @author qingwu
 * 
 */
public class JavaFileVO {

	/** java文件路径. */
	String path;
	/** java文件名称. */
	String name;

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
